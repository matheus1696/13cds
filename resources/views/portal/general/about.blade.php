            <!-- Justificativa -->
            <section class="mb-8">
                <h2 class="text-2xl font-semibold text-blue-700 mb-4">Justificativa</h2>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A Atenção Primária à Saúde (APS), primeiro nível de contato da população com o sistema de saúde,
                    desempenha um papel central na organização e efetividade dos serviços de saúde, sendo implementada
                    no Brasil principalmente por meio da Estratégia Saúde da Família (ESF). Ela foi definida pela
                    primeira vez na Conferência de Alma-Ata, em 1978, como uma atenção essencial à saúde baseada em
                    métodos práticos, acessíveis e universalmente disponíveis, sendo ponto inicial e central dos
                    sistemas de saúde. Posteriormente, no ano de 1994, a pesquisadora estadunidense Starfield ajudou a
                    construir a consolidação a ideia de que a APS promove um cuidado integrado, acessível, contínuo e
                    abrangente, realizado por profissionais que assumem a responsabilidade pela maioria das necessidades
                    de saúde de uma população, em parceria com pacientes, famílias e comunidades (Gusso; Lopes; Dias,
                    2019). Além disso, Starfield (2002) propôs os atributos nucleares da APS, que definem sua qualidade
                    e resolutividade, são eles: atenção ao primeiro contato, coordenação do cuidado, longitudinalidade e
                    integralidade. A atenção ao primeiro contato é o atributo que garante o acesso ao sistema de saúde e
                    considera tanto a facilidade geográfica quanto aspectos organizacionais, como acolhimento e inclusão
                    de populações vulneráveis. A coordenação do cuidado, por sua vez, articula diferentes níveis de
                    atenção, garantindo continuidade e integração do cuidado por meio de instrumentos como referência,
                    contrarreferência e prontuário eletrônico. A longitudinalidade reflete a continuidade do cuidado ao
                    longo do tempo, permitindo um vínculo duradouro entre paciente e equipe, atributo associado a
                    melhores desfechos, como maior adesão a medidas preventivas e menor uso de serviços de alta
                    complexidade. Já a integralidade refere-se à oferta ampla de serviços para atender às diversas
                    necessidades da população, considerando os problemas mais prevalentes na comunidade (Portela, 2017).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Além dos atributos nucleares, a APS possui características derivadas que ampliam sua efetividade,
                    como o foco na família, que aborda o contexto familiar do paciente, orientação comunitária, que
                    adapta o cuidado às necessidades locais, e competência cultural, valorizando o respeito às
                    diversidades culturais e sociais (Gusso; Lopes; Dias, 2019). No Brasil, a Política Nacional de
                    Atenção Básica (PNAB) de 2017 define a APS como “um conjunto de ações integradas que abrangem
                    promoção, prevenção, diagnóstico, tratamento, reabilitação, vigilância e cuidados paliativos,
                    realizadas por equipes multiprofissionais”. Essa definição destaca a importância da APS como pilar
                    de um sistema de saúde centrado nas pessoas, comprometido com a promoção da saúde e a redução das
                    desigualdades sociais (Gusso; Lopes; Dias, 2019).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Para colocar em prática os atributos da APS no Brasil, o Ministério da Saúde, através da Portaria
                    GM/ MS nº 154 de 24 de junho de 2008, ampliou a equipe atuante na APS, estabelecendo os Núcleos de
                    Apoio à Saúde da Família (NASF). Posteriormente, o termo NASF foi substituído para equipe
                    multiprofissional (e-Multi). A Portaria GM/MS nº 635, de 22 de maio de 2023, no que tange às equipes
                    multiprofissionais, explicita que elas são compostas por profissionais de saúde de diferentes áreas
                    de conhecimento que atuam de forma integrada e complementar às outras equipes da Atenção Primária à
                    Saúde (APS) com o intuito de facilitar o acesso da população à saúde. Para isso, promovem ações como
                    o aprimoramento da resolubilidade da APS, a promoção da atenção continuada e a comunicação e a
                    articulação da APS com os outros serviços da Rede de Atenção à Saúde, fundamentando-se nos
                    princípios de universalidade, equidade e integralidade do Sistema Único de Saúde (SUS). Somado a
                    isso, a equipe multiprofissional também pode contribuir para a prevenção de doenças e a promoção de
                    saúde por meio do atendimento preventivo e da educação em saúde (Brasil, 2023; Bispo; Rodrigues,
                    2023). O objetivo é que essa equipe, por meio de uma comunicação eficiente, consiga articular seus
                    saberes e atender às necessidades e individualidades dos pacientes e da comunidade como um todo,
                    integrando-os na produção do cuidado. Assim, o indivíduo é visto como um ser biopsicossocial,
                    levando em conta não apenas seus aspectos físicos, mas também tudo o que o compõe enquanto ser
                    humano (Brasil, 2023; Bispo; Rodrigues, 2023).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A mesma portaria instituiu três modalidades de equipe multiprofissional: a eMulti Ampliada,
                    vinculada a um número entre 10 e 12 equipes de APS, com carga horária mínima de 300 horas e uma
                    composição ampla e diversificada de profissionais, projetada para atender demandas complexas de
                    saúde; a eMulti Complementar, que deve atender de cinco a nove equipes e ser composta por uma carga
                    horária mínima de 200 horas, atuando de forma suplementar às equipes principais e fortalecendo as
                    ações desenvolvidas no território; e a eMulti Estratégica, que deve prestar atendimento de uma a
                    quatro equipes de APS, com carga horária mínima de 100 horas, focando em demandas específicas e
                    estratégicas de saúde pública em contextos particulares. Outro ponto importante é que, enquanto as
                    equipes de APS podem estar vinculadas a apenas uma eMulti, cada equipe multiprofissional pode estar
                    integrada a uma ou mais tipos de equipes da APS, como a Equipe de Saúde da Saúde da Família e a
                    Equipe de Saúde da Família Ribeirinha, o que auxilia ainda mais na abrangência dessas equipes
                    multiprofissionais (Brasil, 2023; Serra et al., 2024).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Uma das ações principais da ação da e-Multi junto à equipe da estratégia de saúde da família é o
                    apoio matricial. O Apoio Matricial é, simultaneamente, uma metodologia de trabalho e um arranjo
                    organizacional que se baseia no Método da Roda (Campos, 2000). Essa abordagem busca proporcionar
                    suporte especializado a equipes de saúde por meio da atuação de profissionais com diferentes
                    formações e especialidades em relação àquelas das equipes apoiadas. Esse suporte ocorre tanto na
                    esfera assistencial quanto no âmbito técnico-pedagógico, promovendo um atendimento mais qualificado
                    e integrado. O modelo tem como princípio a interação entre os profissionais, permitindo uma
                    colaboração mútua e uma ampliação das capacidades de atuação das equipes de referência (Cunha;
                    Campos, 2010; Cunha; Campos, 2011; Campos; Domitti, 2007).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A ideia do apoio matricial através de equipes de referência e apoio especializado foi proposta por
                    Gastão Wagner de Sousa Campos através de um artigo publicado em 1999, no qual descreveu e de
                    justificativa teórica ao conceito, o qual já havia sido implantado pelo autor em três unidades de
                    atenção em saúde localizadas em Campinas/SP e Betim MG. Campos objetivou construir um arranjo
                    organizacional para o trabalho em saúde que fosse condizente com as novas diretrizes de vínculo
                    terapêutico, as quais vinham sendo discutidas durante o século XX e culminaram, no Brasil, na
                    criação do SUS mediante a constituição de 1988 (Campos, 1999).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A implementação do Apoio Matricial no campo da saúde tem o propósito de fortalecer a retaguarda
                    especializada para equipes e profissionais responsáveis pelo cuidado de diferentes problemas de
                    saúde. Esse modelo opera com os conceitos de núcleo e campo, em que um especialista com determinada
                    área de conhecimento colabora com profissionais de outra formação, promovendo maior efetividade no
                    atendimento (Cunha; Campos, 2011). Diferente dos sistemas hierarquizados tradicionais, que se
                    baseiam em mecanismos como referência e contrarreferência, protocolos rígidos e centros de
                    regulação, o Apoio Matricial propõe um modelo complementar. Ele favorece a personalização das
                    relações entre os profissionais, amplia os contextos de atenção especializada e possibilita a
                    construção conjunta de diretrizes clínicas e sanitárias, fortalecendo a integração entre os
                    especialistas e as equipes de referência (Cunha; Campos, 2011).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A história do apoio matricial é intimamente relacionada aos movimentos políticos e sociais que
                    promoveram a mudança do paradigma da saúde, a qual passou a ser enxergada a partir de uma
                    perspectiva transdisciplinar, tendo em vista que a estrutura tradicional dos serviços em saúde
                    fragmenta os processos de trabalho e reforça a hegemonia do poder médico. Assim sendo, diante da
                    complexidade das necessidades do sistema de saúde e das dificuldade de implementar as diretrizes de
                    vínculo terapêutico, os Núcleos de Apoio à Saúde da Família (NASF) foram criados no ano de 2008 a
                    partir do referencial teórico-metodológico do apoio matricial com o intuito de apoiar as equipes da
                    Estratégia de Saúde da Família (Bispo; Moreira, 2018).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Apesar do NASF ser considerado positivo, redefinindo as potencialidades da atenção primária no
                    Brasil, sofreu diversos ataques entre 2016 e 2022, pois governos orientados a uma rígida política de
                    austeridade fiscal atuaram no enfraquecimento da atenção primária, visando movimentos de
                    privatização, tudo isso culminou na interrupção do financiamento do NASF por parte da união e,
                    apenas em Janeiro de 2023, com uma mudança na conformação política do país, uma nova portaria foi
                    publicada, visando ocupar o vazio deixado pelo NASF e instituindo incentivo financeiro para
                    implantação e custeio das equipes multiprofissionais (eMulti) (Bispo; Rodrigues, 2023).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A Portaria GM/MS nº 635, de 22 de maio de 2023, atribui ações a partir das quais as eMulti devem
                    desenvolver a integralidade do cuidado às populações e territórios (Brasil, 2023). Essas ações são
                    ferramentas do matriciamento, aplicáveis de forma articulada e interdisciplinar com vistas à
                    superação da fragmentação da assistência em saúde (Miranda; Cardoso, 2021). Nesse escopo está
                    incluído o atendimento individual, que pode ser compartilhado com outros membros da equipe vinculada
                    à AB ou específico de um dos profissionais da eMulti, em ambos os casos há necessidade de discussão
                    e pactuação conjunta da estratégia terapêutica a ser seguida, contemplando o usuário, sua equipe de
                    referência na AB e o profissional da equipe de apoio (Brasil, 2014).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    As atividades em grupo e atividades coletivas são outras ações úteis ao exercício do cuidado
                    colaborativo no contexto do apoio matricial, com destaque à atuação sobre os aspectos de promoção de
                    saúde, prevenção de agravos e educação em saúde. Essas ferramentas representam a possibilidade de
                    ampliar a resolutividade da APS quando são desenvolvidas com corresponsabilidade entre as equipes de
                    referência e de apoio, a partir das demandas próprias de um território adscrito (Bispo Júnior;
                    Moreira, 2018).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Ainda sobre atividades coletivas, a discussão de casos no apoio matricial engloba encontros entre
                    especialistas e profissionais da APS para analisar conjuntamente situações clínicas complexas. Essa
                    atividade favorece o compartilhamento de conhecimentos e experiências, contribuindo para a
                    construção de projetos terapêuticos singulares, além do fortalecimento das relações
                    interprofissionais e a articulação entre uma abordagem integral e colaborativa no cuidado em saúde
                    (Lima; Dimenstein, 2016).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    No apoio matricial, o atendimento compartilhado é uma ferramenta imprescindível, na medida em que
                    caracteriza-se pela atuação em conjunto de diferentes profissionais de saúde no cuidado ao paciente.
                    Essa abordagem, também conhecida como interconsulta, permite a integração de saberes e práticas,
                    promovendo uma assistência mais integral e resolutiva. Inclusive, o atendimento compartilhado
                    facilita a troca de conhecimentos entre os profissionais envolvidos, promovendo a educação
                    permanente e o aprimoramento das práticas clínicas (Cunha; Campos, 2011; Meireles, et al, 2022).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Outra ferramenta utilizada pelo apoio matricial refere-se a construção conjunta de projetos
                    terapêuticos e intervenção no território. Esse instrumento está relacionado com a tomada de decisões
                    compartilhadas e o reconhecimento de que o paciente e a comunidade são conhecedores de suas
                    demandas. Partindo disso, deve-se buscar articular o conhecimento técnico-profissional com as
                    necessidades específicas de cada paciente de forma a tornar o tratamento adaptado às especificidades
                    socioculturais, econômicas e pessoais de cada indivíduo. Além disso, deve-se reconhecer que a
                    população de cada território possui seu próprio “modus operandi” e que esse está diretamente
                    relacionado com a eficácia da terapêutica aplicada (Brasil, 2023; Oliveira et al, 2025).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Ademais, o apoio matricial também reivindica a ideia de que os diferentes setores da sociedade
                    desempenham um papel na relação do paciente com sua doença e, consequentemente, com o tratamento. A
                    ferramenta da VIII da portaria GM/MS nº 635, de 22 de maio de 2023 determina a importância da
                    comunicação intersetorial na prática clínica, uma vez que as interações sociais e ambientais podem
                    ser facilitadores ou dificultadores da adesão e continuação de tratamentos propostos. Ressalta-se a
                    importância da família, do trabalho e da igreja como agentes determinantes para o sucesso
                    terapêutico do paciente (Garbin; Pintor, 2019).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Além do tema do apoio matricial, a e-Multi atua também como apoiadora de diversas ações na APS, como
                    fortalecer a gestão da clínica, o planejamento estratégico e a gestão participativa. O funcionamento
                    dos serviços de saúde depende de uma micropolítica que reproduz ferramentas de gestão organizadoras
                    do processo de trabalho e mediadoras de conflitos entre a equipe e entre profissionais e usuários
                    (França; Spirandelli, 2020). Nesta perspectiva, a gestão da clínica corresponde a um modelo que
                    articula elementos de atenção à saúde, gestão e educação, objetivando a qualificação dos processos e
                    elevação dos padrões de desempenho clínicos, mediante cuidados qualificados e seguros, e tendo em
                    mente o compartilhamento do poder, valorização dos saberes, experiências e participação dos atores
                    sociais envolvidos (Peixoto et al., 2022).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Nesse sentido, conforme o contexto operado, é imprescindível incorporar o planejamento estratégico
                    no modelo de gestão da Atenção Primária à Saúde (APS) para a efetividade da deliberação clínica.
                    Dessa forma, a partir da análise do ambiente externo e interno, bem como o desenvolvimento ou
                    refinamento de estratégias direcionais organizacionais, é possível estabelecer a interação entre a
                    macrogestão e a vigilância em saúde, sendo capaz de qualificar o atendimento ao usuário e
                    transformar as práticas de trabalho nos serviços de saúde (Lima et al., 2022).
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Por fim, é essencial a reorientação da APS, assumindo como estratégia a ativação de canais de
                    comunicação bilateral para o aperfeiçoamento dos serviços de saúde, mediante a gestão participativa
                    e compartilhada entre a equipe de profissionais organizadores e os usuários de determinada
                    comunidade. Os modelos de gestão participativa são mais adequados ao gerenciamento das unidades
                    básicas de saúde, posto que permitem uma aproximação entre os saberes, as práticas, a cultura e a
                    história de um território-vivo e o modo de organizar e operar as ações de saúde (Gusso; Lopes; Dias,
                    2019; Engstrom; Silva, 2022).
                </p>
            </section>

