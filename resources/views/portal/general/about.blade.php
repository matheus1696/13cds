                <h2 class="text-2xl font-semibold text-blue-700 mb-4">Metodologia e Processo de avaliação </h2>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A metodologia do curso será baseada nos conhecimentos, realidade e experiência prévia dos participantes, priorizando as metodologias ativas de ensino e os ideais da Educação Permanente em Saúde. O curso será semipresencial, com diferentes cargas horárias para diferentes atividades, conforme plano de curso descrito abaixo. A equipe executora do projeto desenvolverá métodos de trabalho em grandes e pequenos grupos nas atividades presenciais uma vez no mês, com o objetivo de gerar discussões sobre o tema e o compartilhamento de experiências. Na atividade assíncrona, será ofertada a oportunidade do participante aprofundar seus conhecimentos sobre o tema através da leitura e fichamento de material disponibilizado pela equipe executora. As atividades práticas envolvem a execução de uma tarefa a ser aplicada no território em que os participantes atuam, sendo um trabalho em equipe. Essa tarefa gerará um relatório que será enviado para a equipe executora.
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    A avaliação dos participantes inclui várias etapas, para dar um panorama multidimensional do desenvolvimento individual e coletivo dos participantes.
                </p>
                <p class="text-justify text-gray-700 leading-relaxed mb-1">
                    Nas atividades presenciais, a avaliação será realizada pelos facilitadores do momento (equipe executora), que irão avaliar a participação das atividades propostas. O fichamento dos textos da atividade assíncrona será avaliado a partir de um formulário padrão de avaliação.
                </p>
